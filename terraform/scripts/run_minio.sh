#!/bin/bash

set -euxo pipefail

export DEBIAN_FRONTEND=noninteractive

mkdir -p ~/minio/data

sudo docker run -d \
   -p 9000:9000 \
   -p 9090:9090 \
   --name minio \
   -v ~/minio/data:/data \
   -e "MINIO_ROOT_USER=ROOTNAME" \
   -e "MINIO_ROOT_PASSWORD=CHANGEME123" \
   quay.io/minio/minio server /data --console-address ":9090"
